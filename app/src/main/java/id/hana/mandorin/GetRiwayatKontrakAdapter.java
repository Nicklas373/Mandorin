package id.hana.mandorin;

public class GetRiwayatKontrakAdapter {

    public String id, nomor_kontrak, nama_pemesan, email, alamat, status, data_pemesan, rekap_data, surat_kontrak, desain_rumah;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id= id;
    }

    public String getNomor_kontrak() {
        return nomor_kontrak;
    }

    public void setNomor_kontrak(String nomor_kontrak) {
        this.nomor_kontrak=nomor_kontrak;
    }

    public String getNama_pemesan() {
        return nama_pemesan;
    }

    public void setNama_pemesan(String nama_pemesan) {
        this.nama_pemesan= nama_pemesan;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email= email;
    }

    public String getAlamat() { return alamat; }

    public void setAlamat(String alamat) { this.alamat= alamat; }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) { this.status= status; }

    public String getData_pemesan() {
        return data_pemesan;
    }

    public void setData_pemesan(String data_pemesan) {
        this.data_pemesan= data_pemesan;
    }

    public String getRekap_data() {
        return rekap_data;
    }

    public void setRekap_data(String rekap_data) {
        this.rekap_data= rekap_data;
    }

    public String getSurat_kontrak() {
        return surat_kontrak;
    }

    public void setSurat_kontrak(String surat_kontrak) {
        this.surat_kontrak= surat_kontrak;
    }

    public String getDesain_rumah() {
        return desain_rumah;
    }

    public void setDesain_rumah(String desain_rumah) {
        this.desain_rumah= desain_rumah;
    }
}