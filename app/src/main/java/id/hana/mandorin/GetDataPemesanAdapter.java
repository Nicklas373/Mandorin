package id.hana.mandorin;

public class GetDataPemesanAdapter {

    public String id, nik, nama, email, no_hp, alamat, tgl_survey, jenis_borongan, data_desain, data_keterangan, status, nik_mandor, nama_mandor;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id= id;
    }

    public String getNik() {
        return nik;
    }

    public void setNik(String nik) {
        this.nik= nik;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama= nama;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email= email;
    }

    public String getNo_hp() {
        return no_hp;
    }

    public void setNo_hp(String no_hp) {
        this.no_hp= no_hp;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat= alamat;
    }

    public String getTgl_survey() {
        return tgl_survey;
    }

    public void setTgl_survey(String tgl_survey) {
        this.tgl_survey= tgl_survey;
    }

    public String getJenis_borongan() {
        return jenis_borongan;
    }

    public void setJenis_borongan(String jenis_borongan) {
        this.jenis_borongan= jenis_borongan;
    }

    public String getData_desain() {
        return data_desain;
    }

    public void setData_desain(String data_desain) {
        this.data_desain= data_desain;
    }

    public String getData_keterangan() {
        return data_keterangan;
    }

    public void setData_keterangan(String data_keterangan) {
        this.data_keterangan= data_keterangan;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status= status;
    }

    public String getNik_mandor() {
        return nik_mandor;
    }

    public void setNik_mandor(String nik_mandor) {
        this.nik_mandor= nik_mandor;
    }

    public String getNama_mandor() {
        return nama_mandor;
    }

    public void setNama_mandor(String nama_mandor) {
        this.nama_mandor= nama_mandor;
    }
}